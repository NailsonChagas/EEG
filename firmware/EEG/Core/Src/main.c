void SendDataTask(void *param) {
	FilteredData ad_data;

	while (1) {
		if (xQueueReceive(queue_tx, &ad_data, portMAX_DELAY) == pdPASS) {
			HAL_UART_Transmit(&hlpuart1, (uint8_t*) &ad_data,
					sizeof(FilteredData), portMAX_DELAY);
			teste++;
		}
		taskYIELD();
	}
}

